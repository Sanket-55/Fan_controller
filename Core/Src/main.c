void Set_Fan_speed()
{
	 float deltaT = fabs(Temperature_sensor - Ambient_sensor_1); // Make delta positive
	 uint8_t duty_cycle = 0;

	 	 if (Temperature_sensor < 30)
	 	 {
	         duty_cycle = 0; // Off
	     }
	     else if (Temperature_sensor < 35)
	     {
	         duty_cycle = 20;
	     }
	     else if (Temperature_sensor < 40)
	     {
	         if (deltaT <= 2.5)
	         {
	             duty_cycle = 20;         // Delta T <= 2.5
	         }
	         else if (deltaT <= 5.0)
	         {
	             duty_cycle = 30;         // 2.5 < Delta T <= 5.0
	         }
	         else if (deltaT <= 7.5)
	         {
	             duty_cycle = 40;         // 5.0 < Delta T <= 7.5
	         }
	         else if (deltaT <= 10)
	         {
	             duty_cycle = 50;         // 7.5 < Delta T <= 10
	         }
	         else if (deltaT <= 12.5)
	         {
	             duty_cycle = 60;         // 10 < Delta T <= 12.5
	         }
	         else
	         {
	             duty_cycle = 80;         // Delta T > 12.5
	         }
	     }
	     else if (Temperature_sensor < 45)
	     {
	         if (deltaT <= 2.5)
	         {
	             duty_cycle = 30;         // Delta T <= 2.5
	         }
	         else if (deltaT <= 5.0)
	         {
	             duty_cycle = 40;         // 2.5 < Delta T <= 5.0
	         }
	         else if (deltaT <= 7.5)
	         {
	             duty_cycle = 70;         // 5.0 < Delta T <= 7.5
	         }
	         else if (deltaT <= 10)
	         {
	             duty_cycle = 80;         // 7.5 < Delta T <= 10
	         }
	         else if (deltaT <= 12.5)
	         {
	             duty_cycle = 90;         // 10 < Delta T <= 12.5
	         }
	         else
	         {
	             duty_cycle = 100;        // Delta T > 12.5
	         }
	     }
	     else if (Temperature_sensor < 50)
	     {
	         if (deltaT <= 2.5)

	         {
	             duty_cycle = 50;         // Delta T <= 2.5
	         }
	         else if (deltaT <= 5.0)
	         {
	             duty_cycle = 80;         // 2.5 < Delta T <= 5.0
	         }
	         else
	         {
	             duty_cycle = 100;        // Delta T > 5.0
	         }
	     }
	     else
	     {
	         if (deltaT < 2.5)
	         {
	             duty_cycle = 70;         // Delta T < 2.5
	         }
	         else
	         {
	             duty_cycle = 100;        // Delta T >= 2.5
	         }
	     }
	 	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, duty_cycle);
	 	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_2, duty_cycle);
	 	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_3, duty_cycle);
	 	__HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_4, duty_cycle);
}



